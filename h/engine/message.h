#ifndef __ENGINE_MESSAGE__
#define __ENGINE_MESSAGE__

#include <types.h>

/*
  Идентификатор сообщения
*/

typedef  long msg_t;

#pragma pack(push,1)

////////////////////////////////////////////////////////////////////////////////////////////
///Описание загловка сообщения
////////////////////////////////////////////////////////////////////////////////////////////
struct    message_t
{
  size_t        size;       //Размер пакета сообщения
  msg_t         msg;        //Идентификатор сообщения 
};

#pragma pack(pop)

///////////////////////////////////////////////////////////////////////////////////////////
///Основные игровые сообщения
///////////////////////////////////////////////////////////////////////////////////////////
enum GameMsg
{
  MSG_GLOBAL_SERVER = 0,
  MSG_LOCAL_SERVER  = 0x1000,
  MSG_USER          = 80000000
};

#endif
#ifndef __ENGINE_SUBSCENE__
#define __ENGINE_SUBSCENE__

#include <pool.h>  

////////////////////////////////////////////////////////////////////////////////////////////
///Подсцена - основная игровая единица
////////////////////////////////////////////////////////////////////////////////////////////
class   SubScene: public MemObject
{
  public:
     virtual             SubScene ();

////////////////////////////////////////////////////////////////////////////////////////////
///Кэширование подсцен
////////////////////////////////////////////////////////////////////////////////////////////
     virtual  bool       lock     ();                      
     virtual  void       unlock   ();

  protected:

////////////////////////////////////////////////////////////////////////////////////////////
///Внутреннее распределение памяти
////////////////////////////////////////////////////////////////////////////////////////////
              void*      alloc    (size_t);
              void       free     (void*);
              Pool*      pool     () const;
              Pool*      M_GetPool () const;

////////////////////////////////////////////////////////////////////////////////////////////
///Загрузка сцены
////////////////////////////////////////////////////////////////////////////////////////////
     virtual  bool       load     ();
     virtual  void       unload   ();             
};

#endif
void    ParseFileName 
( const char* full_name,
  char*       dir_name,
  char*       file_name,
  char*       ext_name
)
{
  static char temp [256];

  char*  ext  = ext_name ? ext_name : temp;
  char*  file = file_name ? file_name : temp;
  char*  dir  = dir_name ? dir_name : temp;

  if (!full_name)
  {
    *ext  = 0;
    *dir  = 0;
    *file = 0;
  }
  else
  {
    const char* s   = full_name;
    const char* old = NULL;

    while (1)
    {
      old = s;
      for (;*s && *s != '\\' && *s != '/';s++);

      if (*s) s++;
      else    break;
    } 

    if (old > full_name)
    {
      memcpy (dir,full_name,old-full_name-1);
      dir [old-full_name-1] = 0;
    }
    else
      *dir = 0;

    s = full_name + strlen (full_name);

    for (;*s != '.' && s != full_name;s--);

    if (*s == '.') strcpy (ext,s+1);
    else           *ext = 0;

    if (s > old)
    {
      memcpy (file,old,s-old);
      file [s-old] = 0;
    }
    else
      *file = 0;
  }
}
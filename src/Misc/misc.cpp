void            memsetw  (void* buf,uint16 v,int n)
{
  uint value = (((uint)v)<<16)+((uint)v);
  uint num   = n >> 1;

  for   (uint* ptr=(uint*)buf;num--;*ptr++=value);
  if    (n&1) *((uint16*)ptr) = v;
}

